    private void btDardeBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDardeBajaActionPerformed
        /**
         * Necesito saber el precio de la actividad que estoy eliminando
         * - Consulto en bd la nomina para coger su precio
         * - Una vez que tenga el precio, tengo que actualizar la n√≥mina
         */
        String consulta = "delete from ACTIVIDAD where ID_ACT = " + txtIDACTIVIDAD.getText();
        String precio="SELECT * FROM ACTIVIDAD WHERE ID_ACT="+ txtIDACTIVIDAD.getText();
        
        //String consulta2="SELECT * FROM NOMINA WHERE ID
        try {
            ActividadController.crearConsultaActividad(precio);
            Actividad actividad= ActividadController.devolverActividad();
            String consultaNomina= "UPDATE NOMINA SET CALCULO_ACTIVIDADES= (CALCULO_ACTIVIDADES - "+ actividad.getPrecio()+"), SALARIOTOTAL=(SALARIOBASE+CALCULO_ACTIVIDADES) WHERE ID = "+actividad.getIdNomina();
            NominasController.ejecutaUpdate(consulta);
            
            try{
                ActividadController.ejecutaUpdate(consultaNomina);
            }catch(SQLException ex) {
                ExceptionController.getError(6, "JPanelAltaBajaActividades.btDardeBajaActionPerformed()");
            }
            JOptionPane.showMessageDialog(null, "Se ha eliminado correctamente la activiadad");
        } catch (SQLException ex) {
            ExceptionController.getError(6, "JPanelAltaBajaActividades.btDardeBajaActionPerformed()");
        }
    }//GEN-LAST:event_btDardeBajaActionPerformed

    private void btGuardarAvtividadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarAvtividadActionPerformed
        String nombre = this.txtNombreAct.getText();
        float precio = Float.valueOf(this.txtPrecoActv.getText());
        int tarea = Integer.valueOf(this.txtTareaAsig.getText());
        int nuevoId = ActividadController.ultimoId();

        String consulta = "INSERT INTO ACTIVIDAD VALUES (" + nuevoId + ", '" + nombre + "', " + precio + ", " + tarea + ", null)";
        try {
            ActividadController.ejecutaUpdate(consulta);
            JOptionPane.showMessageDialog(null, "Se ha insertado correctamente la activiadad");
        } catch (SQLException ex) {
            //Logger.getLogger(JPanelAltaBajaActividades.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Se ha producido un error al intentar insertar la actividad");
        }
    }//GEN-LAST:event_btGuardarAvtividadActionPerformed


